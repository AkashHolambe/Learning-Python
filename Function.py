def subtract(a, b):
    """
    This function subtracts 'b' from 'a' and returns the result.
    
    :param a: The first number (minuend)
    :param b: The second number (subtrahend)
    :return: The result of the subtraction
    """
    result = a - b
    return result

result = subtract(10, 3)
print("Result:", result)






